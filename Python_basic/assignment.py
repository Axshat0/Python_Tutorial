# Multiple assignment
# You can also write it like this
# name = "Me"
# age = 15
# attractive = True
name, age, attractive = "Me", 15, True
print(name)
print(age)
print(attractive)

# Chained assignment
# You can also write it like this
# spongebob = 30
# patrick = 30
# sandy = 30
spongebob = patrick = sandy = 30
print(spongebob)
print(patrick)
print(sandy)
