#Functions
def hello(first_name, last_name, age):
    print("Hello " + first_name + " " + last_name)
    print("You are " + str(age) + " years old")
    print("Have a nice day!")

# Call the 'hello' function with specific values for its parameters
hello("Akshat", "Singh", 15)

# Define variables 'name1', 'name2', and 'x' with values
name1 = "Akshat"
name2 = "Singh"
x = 15

# Call the 'hello' function using the variables as arguments
hello(name1, name2, x)
